package Challange_100_Days;

public class Student
{
    public String Sname;
    public int Smarks;

    public Student(String sname, int smarks)
    {
        Sname = sname;
        Smarks = smarks;
    }

    public String getSname()
    {
        return Sname;
    }

    public int getSmarks()
    {
        return Smarks;
    }
}
